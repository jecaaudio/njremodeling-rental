// Llena un <select> de estados y conecta las sugerencias de ciudad.
window.wireUsLocationFields = function (stateSelect, cityInput, datalist) {
  window.US_STATES.forEach(([code, name]) => {
    const option = document.createElement("option");
    option.value = code;
    option.textContent = `${code} - ${name}`;
    stateSelect.appendChild(option);
  });
  function refreshCities() {
    datalist.innerHTML = "";
    (window.US_CITIES[stateSelect.value] || []).forEach((city) => {
      const option = document.createElement("option");
      option.value = city;
      datalist.appendChild(option);
    });
  }
  stateSelect.addEventListener("change", refreshCities);
  refreshCities();
};
